package com.merkle.wechat.common.entity;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity(name = "token")
public class Token {

    public static final String COMPONENT_ACCESS_TOKEN = "componentAccessToken";
    public static final String PUBLIC_AUTH_TOKEN = "publicAuthToken";
    public static final String MINI_ACCESS_TOKEN = "miniAccessToken";

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    private String appId;

    private String type;

    private Date createdDate = new Date();

    private String accessToken;

    // Just for public
    private String refreshToken;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getAppId() {
        return appId;
    }

    public void setAppId(String appId) {
        this.appId = appId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getRefreshToken() {
        return refreshToken;
    }

    public void setRefreshToken(String refreshToken) {
        this.refreshToken = refreshToken;
    }

}
